let currentSlide = 0

function updateSlide() {
  const slide = slides[currentSlide]
  document.getElementById("slide-title").textContent = slide.title
  document.getElementById("slide-body").innerHTML = slide.content
  document.getElementById("current-slide").textContent = currentSlide + 1

  // Update navigation buttons
  document.getElementById("prev-btn").disabled = currentSlide === 0
  document.getElementById("next-btn").disabled = currentSlide === slides.length - 1

  // Update indicators
  updateIndicators()
  updateOverview()
}

function updateIndicators() {
  const indicatorsContainer = document.getElementById("slide-indicators")
  indicatorsContainer.innerHTML = ""

  slides.forEach((_, index) => {
    const indicator = document.createElement("div")
    indicator.className = `indicator ${index === currentSlide ? "active" : ""}`
    indicator.addEventListener("click", () => goToSlide(index))
    indicatorsContainer.appendChild(indicator)
  })
}

function updateOverview() {
  const overviewContainer = document.getElementById("slide-overview")
  overviewContainer.innerHTML = ""

  slides.forEach((slide, index) => {
    const overviewItem = document.createElement("div")
    overviewItem.className = `overview-item ${index === currentSlide ? "active" : ""}`
    overviewItem.innerHTML = `
            <div class="overview-number">Slide ${slide.id}</div>
            <div class="overview-title">${slide.title}</div>
        `
    overviewItem.addEventListener("click", () => goToSlide(index))
    overviewContainer.appendChild(overviewItem)
  })
}

function nextSlide() {
  if (currentSlide < slides.length - 1) {
    currentSlide++
    updateSlide()
  }
}

function prevSlide() {
  if (currentSlide > 0) {
    currentSlide--
    updateSlide()
  }
}

function goToSlide(index) {
  currentSlide = index
  updateSlide()
}

// Event listeners
document.getElementById("next-btn").addEventListener("click", nextSlide)
document.getElementById("prev-btn").addEventListener("click", prevSlide)

// Keyboard navigation
document.addEventListener("keydown", (e) => {
  if (e.key === "ArrowRight") nextSlide()
  if (e.key === "ArrowLeft") prevSlide()
})

// Initialize
document.getElementById("total-slides").textContent = slides.length
updateSlide()
